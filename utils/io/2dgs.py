    ply_path = model_path / ply_relpath
    if not ply_path.is_file():
        raise FileNotFoundError(
            "2DGS model directory does not contain expected point cloud file: "
            f"{ply_path}"
        )

    gaussians = GaussianModel(sh_degree=3)
